using System;

namespace Trapezoid
{
    class Trapezoid
    {
        static void Main()
        {
            double a = 5;
            double b = 2;
            double h = 4;
            double area = ((a + b) / 2) * h;
            Console.WriteLine(area);

            double a1 = 8.5;
            double b2 = 4.4;
            double h3 = 2;
            double area1 = ((a1 + b2) / 2) * h3;
            Console.WriteLine(area1);
        }
    }
}
